#!/usr/bin/env node
// Local production-mode runner. The repo's next.config.ts sets
// `output: 'standalone'` because production deploys via PM2 from
// `.next/standalone/server.js` (see ecosystem.config.cjs). The
// classic `next start` command is NOT compatible with that output
// — Next 15 prints "next start does not work with output:
// standalone" and refuses to run the standalone bundle.
//
// This script bridges `pnpm start` → standalone server:
//   1. Verify `.next/standalone/server.js` exists (build was run).
//   2. Mirror the production handoff that scripts/deploy.sh
//      performs on the server: copy `.next/static/` + `public/`
//      into `.next/standalone/.next/static` + `.next/standalone/public`
//      so the standalone server can find them at the paths it
//      expects.
//   3. Launch `node .next/standalone/server.js` with PORT + HOSTNAME
//      mirroring ecosystem.config.cjs defaults (loopback bind,
//      port 3040).
//
// Run via `pnpm start`. For dev mode, use `pnpm dev`.

import { existsSync, cpSync, mkdirSync, lstatSync, statSync, realpathSync } from 'node:fs'
import { spawn } from 'node:child_process'
import { join } from 'node:path'

const cwd = process.cwd()
const standaloneDir = join(cwd, '.next', 'standalone')
const serverJs = join(standaloneDir, 'server.js')

if (!existsSync(serverJs)) {
  console.error('[start-standalone] .next/standalone/server.js not found — run `pnpm build` first.')
  process.exit(1)
}

// Copy assets into standalone tree. The standalone build excludes
// these by design (they're large + change per build); the deploy
// script does this on the server, and we mirror locally so
// `pnpm start` boots identically to PM2-in-prod.
// cpSync({ recursive: true, force: true }) is idempotent — safe to
// re-run between local restarts.
const copyPairs = [
  // Static chunks generated by `next build`.
  { src: join(cwd, '.next', 'static'),       dst: join(standaloneDir, '.next', 'static') },
  // Public assets (favicon, robots, etc.).
  { src: join(cwd, 'public'),                dst: join(standaloneDir, 'public') },
  // Schema fingerprint — `instrumentation.ts` reads
  // `process.cwd() + '/db/schema-fingerprint.txt'` at boot and
  // fatals out when missing. Without this, the standalone server
  // logs `schema_fingerprint_check_failed: ENOENT` and aborts.
  // scripts/deploy.sh does the same copy on the prod box (see
  // step 8 in the deploy script header comment).
  { src: join(cwd, 'db', 'schema-fingerprint.txt'),
    dst: join(standaloneDir, 'db', 'schema-fingerprint.txt') },
]

for (const { src, dst } of copyPairs) {
  if (!existsSync(src)) continue
  mkdirSync(join(dst, '..'), { recursive: true })
  // Skip when dst already resolves to src — the in-app updater's
  // tarball-mode extract leaves `.next/standalone/.next/static` as a
  // symlink pointing at the real `.next/static`. cpSync refuses to
  // copy a directory onto its own symlink target with
  // `ERR_FS_CP_EINVAL: src and dest cannot be the same`, crashing
  // the new build's boot and forcing a rollback. realpath both ends
  // and bail out cleanly when they collapse to the same inode.
  try {
    if (existsSync(dst)) {
      const srcReal = realpathSync(src)
      const dstReal = realpathSync(dst)
      if (srcReal === dstReal) {
        console.log(`[start-standalone] skipped ${src.replace(cwd + '/', '')} — already linked to ${dst.replace(cwd + '/', '')}`)
        continue
      }
      // If dst is a symlink (regardless of target), unlink it before
      // cpSync. cpSync on a symlinked dst would try to follow the
      // link and re-cp into the same tree we just resolved to.
      const dstStat = lstatSync(dst)
      if (dstStat.isSymbolicLink()) {
        const { unlinkSync } = await import('node:fs')
        unlinkSync(dst)
      }
    }
  } catch {
    // Defensive: realpath / lstat can throw on broken symlinks or
    // ENOENT mid-flight. Fall through to cpSync — if it fails for a
    // real reason, the catch around the whole loop body would surface.
  }
  cpSync(src, dst, { recursive: true, force: true })
  console.log(`[start-standalone] copied ${src.replace(cwd + '/', '')} → ${dst.replace(cwd + '/', '')}`)
}

// Match the production ecosystem.config.cjs env:
//   HOSTNAME=127.0.0.1 (loopback bind — Next reads HOSTNAME not HOST)
//   PORT=3040
//   CAVECMS_COMMIT=<short SHA>  (lib/env warns when "unknown" in prod —
//                            silence it by deriving from git locally)
// Operator can override any of these via the shell before running.
import { execSync } from 'node:child_process'
let cavecmsCommit = process.env.CAVECMS_COMMIT
if (!cavecmsCommit || cavecmsCommit === 'unknown') {
  try {
    cavecmsCommit = execSync('git rev-parse --short HEAD', { cwd, encoding: 'utf8' }).trim()
  } catch {
    // Not a git checkout (CI tarball / fresh extract). Leave undefined
    // → lib/env's "unknown" fallback fires its one-line warn rather
    // than this script blowing up on the git invocation.
  }
}
const env = {
  ...process.env,
  PORT: process.env.PORT ?? '3040',
  HOSTNAME: process.env.HOSTNAME ?? '127.0.0.1',
  ...(cavecmsCommit ? { CAVECMS_COMMIT: cavecmsCommit } : {}),
}

console.log(`[start-standalone] booting on http://${env.HOSTNAME}:${env.PORT}`)
const child = spawn('node', [serverJs], {
  cwd: standaloneDir,
  env,
  stdio: 'inherit',
})
child.on('exit', (code) => process.exit(code ?? 0))
